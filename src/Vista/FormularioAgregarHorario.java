    public void agregarHorario(){
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String date = sdf.format(fecha.getDate());        
        String fechayhora = String.format("%s %d:%d:00", date,Integer.parseInt(spinHora.getValue().toString()),Integer.parseInt(spinMinutos.getValue().toString()));
        int cupo = Integer.parseInt(spinCupo.getValue().toString());        
        Horario newHorario = new Horario(fechayhora,cupo);
        modeloLista.addElement("Fecha de partida: " + newHorario.getFechayhora() + " Cupo: " + newHorario.getCupo());
        lstHorarios.setModel(modeloLista);
        arreHorario.add(newHorario); 
        
    }    
    
    
