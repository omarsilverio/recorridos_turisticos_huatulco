    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed
        int select = lstHorarios.getSelectedIndex();
        if(select != -1){
            modeloLista.remove(select);
            arreHorario.remove(select);            
            lstHorarios.setModel(modeloLista);
        }
    }//GEN-LAST:event_btnQuitarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(arreHorario.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Agrega algunos horarios");
            return;
        }
        for(Horario h: arreHorario){
            try {
                if(!ServiciosBDD.agregarHorario(ConeccionBDD.obtener(usuario, contrasena), h)){
                    JOptionPane.showMessageDialog(rootPane, "Agrega valores correctos");
                }
            } catch (SQLException ex) {
                Logger.getLogger(FormularioAgregarHorario.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(FormularioAgregarHorario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        this.dispose();        
    }//GEN-LAST:event_btnGuardarActionPerformed

