    public void cargarVentas(){
        MODELOTABLA.setColumnIdentifiers(columnas);
        tablaReservacion.setModel(MODELOTABLA);     
             
       List<Venta> arre = new ArrayList<Venta>();       
       try {
            try {          
                arre = ServiciosBDD.getVentas(ConeccionBDD.obtener(usuario,contrasena));
            } catch (SQLException ex) {
                Logger.getLogger(ConsultarReservcion.class.getName()).log(Level.SEVERE, null, ex);
            }
       } catch (ClassNotFoundException ex) {
           JOptionPane.showMessageDialog(rootPane, "No se pudo establecer la conexi√≥n con la base de datos");
       }
       Object[] datos = new Object[MODELOTABLA.getColumnCount()];       
       for(Venta venta: arre){           
           datos[0] = venta.getIdVenta();
           //JOptionPane.showMessageDialog(rootPane, recorrido.getIdHorario());
           datos[1] = venta.getFecha();
           datos[2] = venta.getMontoTotal();
           datos[3] = venta.getIdReservacion();           
           MODELOTABLA.addRow(datos);
       }
       tablaReservacion.setModel(MODELOTABLA);        
    }
