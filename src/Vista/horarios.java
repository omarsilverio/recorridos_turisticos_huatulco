    public void cargarHorarios() throws SQLException{
        MODELOTABLA.setColumnIdentifiers(columnas);
        tablaHorarios.setModel(MODELOTABLA);     
             
       List<Horario> arre = new ArrayList<Horario>();       
       try {
          arre = ServiciosBDD.getHorarios(ConeccionBDD.obtener(usuario,contrasena));          
       } catch (ClassNotFoundException ex) {
           JOptionPane.showMessageDialog(rootPane, "No se pudo establecer la conexi√≥n con la base de datos");
       }
       Object[] datos = new Object[MODELOTABLA.getColumnCount()];       
       for(Horario horario: arre){           
           datos[0] = horario.getIdHorario();           
           datos[1] = horario.getFechayhora();
           datos[2] = horario.getCupo();           
           MODELOTABLA.addRow(datos);
       }
       tablaHorarios.setModel(MODELOTABLA);    
    }
